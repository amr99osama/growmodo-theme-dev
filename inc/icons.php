 *
 * @param string $name  Icon key.
 * @param string $class CSS class for the svg element.
 * @return string
 */
function re_get_icon( string $name, string $class = 'icon' ): string {
	$lib = re_icon_library();
	if ( empty( $lib[ $name ] ) ) {
		return '';
	}
	return sprintf(
		'<svg class="%1$s" width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="1.8" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true" focusable="false">%2$s</svg>',
		esc_attr( $class ),
		$lib[ $name ] // Static, trusted SVG path markup.
	);
}

